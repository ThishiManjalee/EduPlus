    private void jButtonView1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonView1ActionPerformed

        // TODO add your handling code here:
        String data = jList1.getSelectedValue();

        m.addElement(data);

        jList2.setModel(m);

        DefaultListModel df = (DefaultListModel) jList1.getModel();

        df.remove(jList1.getSelectedIndex());

    }//GEN-LAST:event_jButtonView1ActionPerformed

    private void jButtonView2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonView2ActionPerformed

        DefaultListModel df = (DefaultListModel) jList2.getModel();
        DefaultListModel df1 = (DefaultListModel) jList1.getModel();

        if (JOptionPane.showConfirmDialog(this, "Are you sure to remove selected row ?") == 0) {

            String data = jList2.getSelectedValue();

            df1.addElement(data);

            int i = jList2.getSelectedIndex();

            df.remove(i);
        }


    }//GEN-LAST:event_jButtonView2ActionPerformed

