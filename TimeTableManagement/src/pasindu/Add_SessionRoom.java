    private void jButtonSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSubmitActionPerformed
        // TODO add your handling code here:

        String ch = jComboBoxSessonType.getSelectedItem().toString();

        if ("Consecutive".equals(ch) || "Parallel".equals(ch) || "Non-Overlapping".equals(ch)) {
            try {

                for (int i = 0; i < jList2.getModel().getSize(); i++) {

                    try (Connection cn = connection.DB_Connect.ConnecrDB()) {
                        String val = jList2.getModel().getElementAt(i);
                        String update = "UPDATE session SET roomname = ? WHERE sesname= ? ";
                        
                        PreparedStatement pst = cn.prepareStatement(update);
                        String room = jComboBoxRoom.getSelectedItem().toString();
                        
                        pst.setString(1, room);
                        pst.setString(2, val);
                        
                    if(jComboBoxRoom.getSelectedIndex()==0){
                    
                            JOptionPane.showMessageDialog(this, "Please Select Room");

                    }else{  
                        
                        pst.execute();
                        
                        pst.close();
                    }    
                    }
                }

            } catch (HeadlessException | SQLException e) {
//call NOTNULL validation 
                JOptionPane.showMessageDialog(this, e);
            }

        }if(jComboBoxRoom.getSelectedIndex() !=0){
        
            JOptionPane.showMessageDialog(this, "Added Sucess");
        }

    }//GEN-LAST:event_jButtonSubmitActionPerformed
