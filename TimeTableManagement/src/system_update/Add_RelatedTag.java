    private void jButtonSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSaveActionPerformed
        // TODO add your handling code here:
          try{
           Connection cn=connection.DB_Connect.ConnecrDB();
            
            String sql ="insert into related_tag(name,description) values(?,?)";
       
            PreparedStatement pst=cn.prepareStatement(sql);
            
            pst.setString(1,jTextRelatedTagName.getText() );
            pst.setString(2,ShortDescription.getText());
            
        if(jTextRelatedTagName.getText().trim().isEmpty()||ShortDescription.getText().trim().isEmpty()){
         
                        JOptionPane.showMessageDialog(this, "Text Feild Empty ");

            
        }else{
            
            
            
            pst.execute();
            
            pst.close();
            cn.close();
            JOptionPane.showMessageDialog(this, "Added Sucsessfully");

            
        }


                }catch(HeadlessException | SQLException e){
                
                
                   JOptionPane.showMessageDialog(this, e);
                
                }
        
    }//GEN-LAST:event_jButtonSaveActionPerformed

